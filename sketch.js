function setup() {
	lvl=prompt('mennyire vagy profi? [kezdő,átlagos,profi]','átlagos');
	if(isNaN(lvl)){
		switch(lvl){
			case 'kezdő': v=0.7; break;
			case 'átlagos': v=1.1; break;
			case 'profi': v=2.5; break;
			default : v=1; break;
		}
	}else{
		v=parseInt(lvl);
	}
	v/=2;
	out=document.querySelector('span');
	best=document.querySelector('#highscore');
	fill(255);
	textSize(18);
	var C=createCanvas(windowWidth, 400);
	C.parent('#parent');
	frameRate(60)
	input=document.getElementsByTagName('input')[0];
	input.addEventListener('input',()=>{
		if(keyCode==32){
			check();
		}
	});
	tmpwords=sok.split('\n');
}
function pause(){
	toggle= !toggle;
	(toggle)?loop():noLoop();
}
function reset(){
	words=[];
	score=0;
	out.innerHTML=score;
	toggle=true;
	loop();
}
function draw() {
  background(0);
  if(tmpwords.length){
		if(frameCount%200===0){
			words.push(new Text(random(tmpwords)));
		}
		for(let word of words){
			word.x+=v;
			word.show();
			if(word.x>width){
				noLoop();
				alert('Vesztettél!');
			}
		}
  }
}

class Text{
	constructor(value){
		this.x=-30;
		this.y=floor(random(sav))*height/sav+sav;
		this.value=value;
	}
	show(){
		text(this.value,this.x,this.y);
	}
}

function check(){
 let search=input.value.toString().substring(0,input.value.toString().length-1);
 input.value='';
 for(let i=words.length-1;i>=0;i--){
	 if(search===words[i].value){
			words.splice(i,1);
			score++;
			if(highest<score){
				highest=score;
				best.innerHTML=highest;
			}
			out.innerHTML=score;
			return;
	 } 
 }
}

